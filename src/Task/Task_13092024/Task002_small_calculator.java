package Task.Task_13092024;


public class Task002_small_calculator {
    public static void main(String[] args) {
        double a = 34;
        double b = 10;

        double add = a + b;
        double sub = a - b;
        double multi = a * b;
        double div = a / b;
        double modulus = a % b;

        System.out.printf("%f + %f = %f",a,b,add);
        System.out.println();
        System.out.printf("%f - %f = %f",a,b,sub);
        System.out.println();
        System.out.printf("%f * %f = %f",a,b,multi);
        System.out.println();
        System.out.printf("%f / %f = %f",a,b,div);
        System.out.println();
        System.out.printf("%f %%  %f = %f",a,b,modulus);
        System.out.println();






    }
}
