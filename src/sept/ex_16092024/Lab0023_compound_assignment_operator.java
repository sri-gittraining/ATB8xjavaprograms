package sept.ex_16092024;

public class Lab0023_compound_assignment_operator {
    public static void main(String[] args) {
        //compound assignment operator
        //+=,-=,*=,/=
        int a = 10;
        int b = 20;
        int c = 30;
        a += 20;//a =a + 20
        b -= 20;//b = b - 20
        c *= 20;//c = c * 20
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
