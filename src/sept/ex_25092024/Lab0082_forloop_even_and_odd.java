package sept.ex_25092024;

public class Lab0082_forloop_even_and_odd {
    public static void main(String[] args) {
        for (int i = 0; i <=50 ; i++) {
            if( i%2 ==0){
                System.out.println("Even Number "+i);
            }
            if(i%2 !=0){
                System.out.println("Odd Number "+i);
            }

        }
    }
}
